def get_default_vba_project_bin() -> bytes:
    """
    Return bundled VBA project binary (xl/vbaProject.bin).

    Note: The bundled binary is patched at runtime to remove incompatible modules/controls that can cause
    macOS Excel to show an empty macro list (even though VBA exists).
    """

    global _DEFAULT_VBA_PROJECT_BIN_CACHE  # noqa: PLW0603
    if _DEFAULT_VBA_PROJECT_BIN_CACHE is not None:
        return _DEFAULT_VBA_PROJECT_BIN_CACHE

    data = base64.b64decode(_DEFAULT_VBA_PROJECT_BIN_ZLIB_B64)
    raw = zlib.decompress(data)
    patched = _patch_default_vba_project_bin(raw)
    _DEFAULT_VBA_PROJECT_BIN_CACHE = patched
    return patched


def _patch_default_vba_project_bin(vba_project_bin: bytes) -> bytes:
    """
    Best-effort patch of the bundled vbaProject.bin.

    The original donor VBA project contained extra UserForms/Ribbon helpers and worksheet ActiveX control
    attributes that are irrelevant to this project, but can prevent Excel (notably macOS builds) from loading
    the VBProject correctly.

    Constraints:
    - We must keep every OLE stream size unchanged (olefile only supports in-place writes).
    """
    try:
        import olefile  # type: ignore[import-not-found]
    except Exception:
        return vba_project_bin

    from .msovba import decompress_stream

    def _patch_msovba_compressed_stream_literals(
        compressed_container: bytes, *, patches_by_decompressed_pos: dict[int, int]
    ) -> bytes | None:
        """
        Patch specific bytes in a MS-OVBA compressed container by specifying decompressed offsets.

        This works by instrumenting the decompressor to map decompressed bytes back to the input positions
        that provided their literal values, then mutating those literal bytes in-place.

        Returns patched bytes on success, else None.
        """
        if not patches_by_decompressed_pos:
            return compressed_container
        if not compressed_container or compressed_container[0] != 0x01:
            return None

        buf = bytearray(compressed_container)

        # origin[i] >= 0: literal byte came from compressed_container[origin[i]]
        # origin[i] < 0: byte was copied from decompressed index (-1 - origin[i])
        origin: list[int] = []

        compressed_current = 0
        sig = buf[compressed_current]
        if sig != 0x01:
            return None
        compressed_current += 1

        def resolve_literal_origin(decomp_index: int) -> int | None:
            try:
                o = origin[decomp_index]
            except Exception:
                return None
            # Chase copy chains
            while o < 0:
                decomp_index = -1 - o
                if decomp_index < 0 or decomp_index >= len(origin):
                    return None
                o = origin[decomp_index]
            return o

        def copytoken_help(decompressed_current: int, decompressed_chunk_start: int) -> tuple[int, int, int]:
            # Local minimal port (avoid importing math for tiny helper).
            difference = decompressed_current - decompressed_chunk_start
            if difference <= 0:
                bit_count = 4
            else:
                # ceil(log2(difference))
                bit_count = (difference - 1).bit_length()
                bit_count = max(bit_count, 4)
            length_mask = 0xFFFF >> bit_count
            offset_mask = (~length_mask) & 0xFFFF
            return length_mask, offset_mask, bit_count

        try:
            while compressed_current < len(buf):
                compressed_chunk_start = compressed_current
                if compressed_chunk_start + 2 > len(buf):
                    break

                header = struct.unpack_from("<H", buf, compressed_chunk_start)[0]
                chunk_size = (header & 0x0FFF) + 3
                chunk_signature = (header >> 12) & 0x07
                if chunk_signature != 0b011:
                    return None
                chunk_flag = (header >> 15) & 0x01

                compressed_end = min(len(buf), compressed_chunk_start + chunk_size)
                compressed_current = compressed_chunk_start + 2

                if chunk_flag == 0:
                    # Uncompressed chunk: 4096 literal bytes
                    take = min(4096, compressed_end - compressed_current)
                    for j in range(take):
                        origin.append(compressed_current + j)
                    compressed_current += take
                    continue

                decompressed_chunk_start = len(origin)
                while compressed_current < compressed_end:
                    flag_byte = buf[compressed_current]
                    compressed_current += 1
                    for bit_index in range(8):
                        if compressed_current >= compressed_end:
                            break
                        flag_bit = (flag_byte >> bit_index) & 1
                        if flag_bit == 0:
                            # LiteralToken
                            origin.append(compressed_current)
                            compressed_current += 1
                        else:
                            if compressed_current + 2 > compressed_end:
                                return None
                            copy_token = struct.unpack_from("<H", buf, compressed_current)[0]
                            length_mask, offset_mask, bit_count = copytoken_help(len(origin), decompressed_chunk_start)
                            length = (copy_token & length_mask) + 3
                            temp1 = copy_token & offset_mask
                            temp2 = 16 - bit_count
                            offset = (temp1 >> temp2) + 1
                            copy_source = len(origin) - offset
                            for k in range(length):
                                src = copy_source + k
                                origin.append(-1 - src)
                            compressed_current += 2
        except Exception:
            return None

        for decomp_pos, new_byte in sorted(patches_by_decompressed_pos.items()):
            lit = resolve_literal_origin(int(decomp_pos))
            if lit is None:
                return None
            if lit <= 0 or lit >= len(buf):
                return None
            buf[lit] = int(new_byte) & 0xFF

        # Validate that decompression still works and patches took effect.
        try:
            _ = decompress_stream(bytes(buf))
        except Exception:
            return None
        return bytes(buf)

    def find_source_offset(stream_bytes: bytes) -> int:
        idx = stream_bytes.lower().find(b"\x00attribut")
        if idx < 0:
            raise ValueError("attribute marker not found")
        start = idx - 3
        if start < 0 or stream_bytes[start] != 0x01:
            raise ValueError("VBA compressed stream signature not found")
        return start

    def compress_stream_literal(data: bytes) -> bytes:
        out = bytearray()
        out.append(0x01)  # signature

        i = 0
        while i < len(data):
            remaining = len(data) - i
            n = min(4096, remaining)
            # Ensure compressed chunk size <= 4098 for chunk_flag=1 when encoding everything as literals.
            while n > 0 and (2 + n + ((n + 7) // 8)) > 4098:
                n -= 1
            if n <= 0:
                raise ValueError("failed to choose chunk size for VBA compression")

            chunk = data[i : i + n]
            i += n

            comp = bytearray()
            for j in range(0, len(chunk), 8):
                group = chunk[j : j + 8]
                comp.append(0x00)  # flag byte: all LiteralToken
                comp.extend(group)

            chunk_size = 2 + len(comp)
            header = (chunk_size - 3) | (0b011 << 12) | (1 << 15)
            out.extend(struct.pack("<H", header))
            out.extend(comp)

        return bytes(out)

    def build_container_to_exact_len(source: bytes, target_len: int) -> bytes:
        if not source.endswith(b"\r\n"):
            source += b"\r\n"

        pad_line_prefix = b"'pad"

        def comp(src: bytes) -> bytes:
            return compress_stream_literal(src)

        cur = source
        container = comp(cur)
        if len(container) > target_len:
            raise ValueError(f"base container too large: {len(container)} > {target_len}")

        pad_block = (pad_line_prefix + b"\r\n") * 16
        while len(container) < target_len:
            cur += pad_block
            container = comp(cur)

        if len(container) == target_len:
            return container

        # Step back to <= target and fine tune with a variable-length final line.
        while cur.endswith(pad_block):
            trial = cur[: -len(pad_block)]
            trial_container = comp(trial)
            if len(trial_container) <= target_len:
                cur = trial
                container = trial_container
                break
            cur = trial
            container = trial_container

        base = cur
        low, high = 0, 4096
        best_mid = None
        while low <= high:
            mid = (low + high) // 2
            line = pad_line_prefix + (b"X" * mid) + b"\r\n"
            trial_container = comp(base + line)
            L = len(trial_container)
            if L == target_len:
                return trial_container
            if L < target_len:
                best_mid = mid
                low = mid + 1
            else:
                high = mid - 1

        if best_mid is None:
            raise ValueError("failed to pad VBA container")

        for mid in range(max(0, best_mid - 64), best_mid + 65):
            line = pad_line_prefix + (b"X" * mid) + b"\r\n"
            trial_container = comp(base + line)
            if len(trial_container) == target_len:
                return trial_container

        raise ValueError("could not hit exact VBA container length")

    def make_stub_source(src: bytes, *, strip_vb_control: bool, keep_keys: set[str] | None) -> bytes:
        text = src.decode("cp1252", errors="replace")
        lines = text.splitlines()

        attrs: list[str] = []
        for line in lines:
            if line.startswith("Attribute "):
                if strip_vb_control and line.startswith("Attribute VB_Control"):
                    continue
                if keep_keys is not None:
                    parts = line.split(" ", 2)
                    if len(parts) >= 2:
                        key = parts[1]
                        if key not in keep_keys:
                            continue
                attrs.append(line)
                continue
            if line.strip() == "Option Explicit":
                break

        # Ensure VB_Name is always present when filtering.
        if keep_keys is not None and not any(l.startswith("Attribute VB_Name") for l in attrs):
            for line in lines:
                if line.startswith("Attribute VB_Name"):
                    attrs.insert(0, line)
                    break

        out_lines = [*attrs, "Option Explicit", ""]
        out_text = "\r\n".join(out_lines) + "\r\n"
        return out_text.encode("cp1252", errors="replace")

    # Patch a small set of donor modules to avoid Excel load failures on macOS.
    modules_to_stub = {"AutoComp", "EditNoncomps", "Sorting", "Formatting", "Sheet1"}

    buf = io.BytesIO(bytearray(vba_project_bin))
    try:
        ole = olefile.OleFileIO(buf, write_mode=True)
    except Exception:
        return vba_project_bin

    try:
        # 1) Fix MODULEPRIVATE on the VBA dir stream.
        #
        # The donor project had the actual entry macro inside a module marked as "private" at the VBA project
        # level. Excel's macro list UI (especially on macOS) does not enumerate macros from private modules,
        # resulting in an empty macro list even though macros exist.
        try:
            dir_path = ["VBA", "dir"]
            dir_comp = ole.openstream(dir_path).read()
            dir_raw = decompress_stream(dir_comp)

            # ModulePrivate record is: 0x0028 + reserved(4 bytes). It's typically directly followed by TERMINATOR.
            # Patch it to MODULEREADONLY (0x0025) which is benign and keeps the byte length identical.
            priv_pat = b"\x28\x00\x00\x00\x00\x00\x2B\x00\x00\x00\x00\x00"
            patch_positions: dict[int, int] = {}
            start = 0
            while True:
                idx = dir_raw.find(priv_pat, start)
                if idx < 0:
                    break
                patch_positions[idx] = 0x25  # change record id only
                start = idx + 1

            if patch_positions:
                patched_dir = _patch_msovba_compressed_stream_literals(
                    dir_comp,
                    patches_by_decompressed_pos=patch_positions,
                )
                if patched_dir is not None and patched_dir != dir_comp and len(patched_dir) == len(dir_comp):
                    ole.write_stream(dir_path, patched_dir)
        except Exception:
            pass

        # 2) Replace the legacy macOS runner (MacScript/do shell script) with a Shell-based runner.
        #
        # Some environments (notably localized/macOS hardened configurations) fail to compile/run MacScript,
        # which results in runtime error 5 ("Invalid procedure call or argument"). Shell + zsh login shell is
        # more robust and also fixes PATH issues for python.org/Homebrew installs.
        try:
            stream_path = ["VBA", "Calculations"]
            stream = ole.openstream(stream_path).read()
            code_offset = find_source_offset(stream)
            code_len = len(stream) - code_offset
            src = decompress_stream(stream[code_offset:])

            # Always ensure the macro runner matches the latest implementation.
            # (Older donors used MacScript/do shell script; newer ones may already be Shell-based,
            # but we still want add-in compatibility: ActiveWorkbook fallback and meta sheet probing.)
            if b"ClearMLDatasetExcel_Run" in src:
                addin_ver = str(_ADDIN_VERSION).replace('"', '""')
                calc_source = (
                    'Attribute VB_Name = "Calculations"\r\n'
                    "Option Explicit\r\n"
                    'Private Const M$ = "_meta"\r\n'
                    'Private Const L$ = "clearml_dataset_excel_addin.log"\r\n'
                    f'Private Const V$ = "{addin_ver}"\r\n'
                    "\r\n"
                    "Public Sub ClearMLDatasetExcel_Run()\r\n"
                    "On Error Resume Next\r\n"
                    "Dim twb As Workbook, d$, sf$, sp$, t$, c$\r\n"
                    'Set twb = TW(): If twb Is Nothing Then MsgBox "No meta": Exit Sub\r\n'
                    'If Not En(Meta(twb, "addin_enabled")) Then Exit Sub\r\n'
                    't = Meta(twb, "addin_version"): If t <> "" And t <> V Then MsgBox "Version mismatch"\r\n'
                    'd = twb.Path: If d = "" Then MsgBox "Save": Exit Sub\r\n'
                    'sf = Meta(twb, "addin_spec_filename"): If sf = "" Then Exit Sub\r\n'
                    "#If Mac Then\r\n"
                    'sp = d & "/" & sf: t = Meta(twb, "addin_command_mac")\r\n'
                    "#Else\r\n"
                    'sp = d & Application.PathSeparator & sf: t = Meta(twb, "addin_command_windows")\r\n'
                    "#End If\r\n"
                    'If t = "" Then t = Meta(twb, "addin_command")\r\n'
                    'If t = "" Then t = "clearml-dataset-excel run --spec ""${SPEC}"" --excel ""${EXCEL}"""\r\n'
                    'c = Replace(Replace(t, "${SPEC}", sp), "${EXCEL}", twb.FullName)\r\n'
                    "#If Mac Then\r\n"
                    "MacRun c, d\r\n"
                    "#Else\r\n"
                    "WinRun c, d\r\n"
                    "#End If\r\n"
                    "End Sub\r\n"
                    "\r\n"
                    "Sub ClearMLDatasetExcel_Run_Ribbon(o)\r\n"
                    "ClearMLDatasetExcel_Run\r\n"
                    "End Sub\r\n"
                    "\r\n"
                    "Private Function En(ByVal s$) As Boolean\r\n"
                    "s = LCase$(s)\r\n"
                    'En = (s = "true" Or s = "1" Or s = "yes")\r\n'
                    "End Function\r\n"
                    "\r\n"
                    "Private Function TW() As Workbook\r\n"
                    "On Error Resume Next\r\n"
                    "Dim ws As Worksheet\r\n"
                    "Set ws = ThisWorkbook.Worksheets(M): If Not ws Is Nothing Then Set TW = ThisWorkbook: Exit Function\r\n"
                    "If Not ActiveWorkbook Is Nothing Then Set ws = ActiveWorkbook.Worksheets(M): If Not ws Is Nothing Then Set TW = ActiveWorkbook\r\n"
                    "End Function\r\n"
                    "\r\n"
                    "#If Mac Then\r\n"
                    "Private Sub MacRun(ByVal c$, ByVal d$)\r\n"
                    "On Error Resume Next\r\n"
                    "Dim lp$, w$, sc$\r\n"
                    'lp = d & "/" & L\r\n'
                    'w = "(" & c & ") > " & Q(lp) & " 2>&1 &"\r\n'
                    'sc = "/bin/zsh -lc " & Q(w)\r\n'
                    "Shell sc\r\n"
                    "End Sub\r\n"
                    "\r\n"
                    "Private Function Q(ByVal s$) As String\r\n"
                    'Q = Chr(39) & Replace(s, Chr(39), Chr(39) & Chr(34) & Chr(39) & Chr(34) & Chr(39)) & Chr(39)\r\n'
                    "End Function\r\n"
                    "#End If\r\n"
                    "\r\n"
                    "Private Sub WinRun(ByVal c$, ByVal d$)\r\n"
                    "On Error Resume Next\r\n"
                    "Dim lp$\r\n"
                    "lp = d & Application.PathSeparator & L\r\n"
                    'CreateObject("WScript.Shell").Run "cmd.exe /c cd /d """&d&"""&&("&c&")>"""&lp&""" 2>&1",0,False\r\n'
                    "MsgBox lp\r\n"
                    "End Sub\r\n"
                    "\r\n"
                    "Private Function Meta(ByVal wb As Workbook, ByVal k$) As String\r\n"
                    "On Error Resume Next\r\n"
                    "Dim r As Range\r\n"
                    "Set r = wb.Worksheets(M).Columns(1).Find(What:=k, LookIn:=xlValues, LookAt:=xlWhole)\r\n"
                    'If r Is Nothing Then Meta = "" Else Meta = CStr(r.Offset(0, 1).Value)\r\n'
                    "End Function\r\n"
                ).encode("cp1252", errors="replace")

                if len(compress_stream_literal(calc_source)) <= code_len:
                    container = build_container_to_exact_len(calc_source, code_len)
                    new_stream = stream[:code_offset] + container
                    if len(new_stream) == len(stream):
                        ole.write_stream(stream_path, new_stream)
        except Exception:
            pass

        # 3) Strip/neutralize donor modules that cause Excel to fail loading the project on macOS.
        for mod in modules_to_stub:
            stream_path = ["VBA", mod]
            try:
                stream = ole.openstream(stream_path).read()
            except Exception:
                continue

            try:
                code_offset = find_source_offset(stream)
                code_len = len(stream) - code_offset
                src = decompress_stream(stream[code_offset:])
            except Exception:
                continue

            if mod == "Sheet1":
                # The donor's Sheet1 module included ActiveX controls (VB_Control attributes).
                # Keep a minimal attribute set to fit the tiny code segment.
                keep = {"VB_Name", "VB_Base", "VB_PredeclaredId", "VB_Exposed"}
                stub = make_stub_source(src, strip_vb_control=True, keep_keys=keep)
                if len(compress_stream_literal(stub)) > code_len:
                    stub = make_stub_source(src, strip_vb_control=True, keep_keys={"VB_Name"})
            else:
                stub = make_stub_source(src, strip_vb_control=False, keep_keys=None)

            try:
                container = build_container_to_exact_len(stub, code_len)
            except Exception:
                continue

            new_stream = stream[:code_offset] + container
            if len(new_stream) != len(stream):
                continue
            try:
                ole.write_stream(stream_path, new_stream)
            except Exception:
                continue
    finally:
        try:
            ole.close()
        except Exception:
            pass

    try:
        return buf.getvalue()
    except Exception:
        return vba_project_bin


def patch_vba_project_bin_for_excel_compat(vba_project_bin: bytes) -> bytes:
    """
    Patch a vbaProject.bin for better Excel compatibility (best-effort).

    This is mainly intended to repair older templates that were generated with the bundled donor VBA project.
    """
    return _patch_default_vba_project_bin(vba_project_bin)
